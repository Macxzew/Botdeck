export function i18nText(source: string): string {
	const language = typeof document !== "undefined"
		? normalizeUiLanguage(document.documentElement.lang) ?? readUiLanguage()
		: "fr";
	const catalog: Record<string, string> = inlineI18nText[language];
	return catalog[source] ?? source;
}

