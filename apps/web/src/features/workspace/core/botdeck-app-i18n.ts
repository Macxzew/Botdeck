// Internationalisation Botdeck

"use client";

export type UiLanguage = "fr" | "en";
export const uiLanguageStorageKey = "botdeck:ui-language";

// Normalise la langue UI.
export function normalizeUiLanguage(value: string | null | undefined): UiLanguage | null {
	if (!value) return null;
	const normalized = value.toLowerCase();
	if (normalized.startsWith("fr")) return "fr";
	if (normalized.startsWith("en")) return "en";
	return null;
}

// Détecte la langue système.
export function detectSystemLanguage(): UiLanguage {
	if (typeof window === "undefined") return "fr";
	// Langue système par défaut.
	return normalizeUiLanguage(window.navigator.language) ?? normalizeUiLanguage(window.navigator.languages?.[0]) ?? "en";
}

// Lit la langue UI.
export function readUiLanguage(): UiLanguage {
	if (typeof window === "undefined") return "fr";
	// Préférence utilisateur prioritaire.
	const saved = normalizeUiLanguage(window.localStorage.getItem(uiLanguageStorageKey));
	return saved ?? detectSystemLanguage();
}

// Écrit la langue UI.
export function writeUiLanguage(language: UiLanguage): void {
	window.localStorage.setItem(uiLanguageStorageKey, language);
	document.documentElement.lang = language;
}

